"""
Write a program that uses exactly four for loops to print the sequence of letters below.
"""


def numbers():
    for i in range(10):
        print("A", end=" ")
    for j in range(7):
        print("B", end=" ")
    for m in range(5):
        print("C", end=" ")
        print("D", end=" ")
    print("E", end=" ")

